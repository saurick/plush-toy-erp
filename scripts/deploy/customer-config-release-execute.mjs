function requireOption(options, key) {
  if (!options[key]) {
    throw new CliError(
      `Missing required --${key.replace(/[A-Z]/g, (ch) => `-${ch.toLowerCase()}`)}`,
      2,
    );
  }
}

async function readJson(filePath) {
  return JSON.parse(await readFile(filePath, "utf8"));
}

async function writeJson(filePath, data) {
  await writeFile(filePath, `${JSON.stringify(data, null, 2)}\n`, "utf8");
}

function sha256File(filePath) {
  return crypto
    .createHash("sha256")
    .update(fs.readFileSync(filePath))
    .digest("hex");
}

function normalizeBaseURL(raw) {
  const url = new URL(String(raw || "").trim());
  if (url.username || url.password) {
    throw new CliError("backend URL must not contain username or password");
  }
  url.pathname = url.pathname.replace(/\/+$/, "");
  url.search = "";
  url.hash = "";
  return url.toString().replace(/\/+$/, "");
}

function manifestSummary(manifest) {
  return {
    customerKey: manifest.customer_key,
    revision: manifest.revision,
    productVersion: manifest.product_version,
    moduleStateCount: manifest.module_states.length,
    roleProfileCount: manifest.role_profiles.length,
    entitlementCount: manifest.access_entitlements.length,
    workPoolCount: manifest.work_pools.length,
    membershipCount: manifest.work_pool_memberships.length,
    pageCount: manifest.compiled_snapshot.pages.length,
  };
}

function buildOperations({ manifest, activate, activateOnly, rollback }) {
  if (rollback) {
    return [
      {
        key: "rollback",
        rpcPath: "/rpc/customer_config",
        method: "rollback_customer_config",
        params: {
          customer_key: manifest.customer_key,
          target_revision: manifest.revision,
        },
        summary: "roll back active customer config to the manifest revision",
      },
    ];
  }
  if (activateOnly) {
    return [
      {
        key: "activate",
        rpcPath: "/rpc/customer_config",
        method: "activate_customer_config",
        params: {
          customer_key: manifest.customer_key,
          revision: manifest.revision,
        },
        summary: "activate already published customer config revision",
      },
    ];
  }
  const operations = [
    {
      key: "validate",
      rpcPath: "/rpc/customer_config",
      method: "validate_customer_config",
      params: manifest,
      summary: "validate compiled customer config manifest",
    },
    {
      key: "publish",
      rpcPath: "/rpc/customer_config",
      method: "publish_customer_config",
      params: manifest,
      summary: "publish compiled customer config revision",
    },
  ];
  if (activate) {
    operations.push({
      key: "activate",
      rpcPath: "/rpc/customer_config",
      method: "activate_customer_config",
      params: {
        customer_key: manifest.customer_key,
        revision: manifest.revision,
      },
      summary: "activate published customer config revision",
    });
  }
  return operations;
}

function arrayFrom(value) {
  return Array.isArray(value) ? value.filter((item) => typeof item === "string" && item.trim() !== "") : [];
}

function sortedKeys(value) {
  if (!value || typeof value !== "object" || Array.isArray(value)) {
    return [];
  }
  return Object.keys(value).sort();
}

function sameStringSet(left, right) {
  if (left.length !== right.length) {
    return false;
  }
  return left.every((item, index) => item === right[index]);
}

async function rpcCall({ backendURL, token, rpcPath, method, params }) {
  const response = await fetch(new URL(rpcPath, `${backendURL}/`).toString(), {
    method: "POST",
    headers: {
      Accept: "application/json",
      "Content-Type": "application/json",
      ...(token ? { Authorization: `Bearer ${token}` } : {}),
    },
    body: JSON.stringify({
      jsonrpc: "2.0",
      id: `customer-config-${method}-${Date.now()}`,
      method,
      params,
    }),
  });
  if (!response.ok) {
    throw new CliError(`${method} HTTP ${response.status}`);
  }
  const json = await response.json();
  if (json?.result?.code !== 0) {
    throw new CliError(
      `${method} code=${json?.result?.code} message=${json?.result?.message}`,
    );
  }
  return json.result.data || {};
}

async function verifyEffectiveSession({ backendURL, token, manifest }) {
  const data = await rpcCall({
    backendURL,
    token,
    rpcPath: "/rpc/customer_config",
    method: "get_effective_session",
    params: { customer_key: manifest.customer_key },
  });
  const session = data.session || data;
  const pages = arrayFrom(session.pages);
  const actions = arrayFrom(session.actions);
  const workPools = arrayFrom(session.workPools);
  const manifestPages = arrayFrom(manifest.compiled_snapshot?.pages);
  const manifestFieldPolicySurfaces = sortedKeys(manifest.compiled_snapshot?.fieldPolicies);
  const sessionFieldPolicySurfaces = sortedKeys(session.fieldPolicies);
  const pagesSubsetOfManifest = pages.every((pageKey) => manifestPages.includes(pageKey));
  const fieldPolicySurfacesMatchManifest = sameStringSet(
    sessionFieldPolicySurfaces,
    manifestFieldPolicySurfaces,
  );

  if (session.customer?.key !== manifest.customer_key) {
    throw new CliError("effective session customer does not match manifest");
  }
  if (session.configRevision !== manifest.revision) {
    throw new CliError("effective session configRevision does not match manifest revision");
  }
  if (session.source !== "active_customer_config_revision") {
    throw new CliError("effective session source is not active_customer_config_revision");
  }
  if (pages.length === 0) {
    throw new CliError("effective session pages must not be empty after activation");
  }
  if (!pagesSubsetOfManifest) {
    throw new CliError("effective session pages must be a subset of manifest pages");
  }
  if (!fieldPolicySurfacesMatchManifest) {
    throw new CliError("effective session field policy surfaces do not match manifest");
  }

  return {
    status: "verified",
    method: "get_effective_session",
    customerKey: session.customer?.key || "",
    revision: manifest.revision,
    configRevision: session.configRevision || "",
    source: session.source || "",
    pageCount: pages.length,
    actionCount: actions.length,
    workPoolCount: workPools.length,
    fieldPolicySurfaceCount: sessionFieldPolicySurfaces.length,
    pagesSubsetOfManifest,
    fieldPolicySurfacesMatchManifest,
  };
}

async function resolveAdminToken(backendURL) {
  if (process.env.CUSTOMER_CONFIG_ADMIN_TOKEN) {
    return process.env.CUSTOMER_CONFIG_ADMIN_TOKEN;
  }
  const username = process.env.CUSTOMER_CONFIG_ADMIN_USERNAME;
  const password = process.env.CUSTOMER_CONFIG_ADMIN_PASSWORD;
  if (!username || !password) {
    throw new CliError(
      "execution requires CUSTOMER_CONFIG_ADMIN_TOKEN or CUSTOMER_CONFIG_ADMIN_USERNAME/CUSTOMER_CONFIG_ADMIN_PASSWORD",
    );
  }
  const data = await rpcCall({
    backendURL,
    rpcPath: "/rpc/auth",
    method: "admin_login",
    params: { username, password },
  });
  const token = data.access_token || data.token;
  if (!token) {
    throw new CliError("admin_login response missing access token");
  }
  return token;
}

function requireExecutionConfirmation({ activate, rollback }) {
  let expected = PUBLISH_CONFIRM_PHRASE;
  if (activate) {
    expected = ACTIVATE_CONFIRM_PHRASE;
  }
  if (rollback) {
    expected = ROLLBACK_CONFIRM_PHRASE;
  }
  if (process.env.CUSTOMER_CONFIG_CONFIRM !== expected) {
    throw new CliError(`execution requires CUSTOMER_CONFIG_CONFIRM=${expected}`);
  }
}

async function executeOperations({ backendURL, operations, manifest, activate, rollback }) {
  requireExecutionConfirmation({ activate, rollback });
  const token = await resolveAdminToken(backendURL);
  const results = [];
  for (const operation of operations) {
    const data = await rpcCall({
      backendURL,
      token,
      rpcPath: operation.rpcPath,
      method: operation.method,
      params: operation.params,
    });
    results.push({
      key: operation.key,
      method: operation.method,
      resultRevision:
        data?.revision?.revision || data?.validation?.revision || null,
      resultStatus: data?.revision?.status || null,
    });
  }
  const effectiveSessionVerification =
    activate || rollback
      ? await verifyEffectiveSession({ backendURL, token, manifest })
      : null;
  return { results, effectiveSessionVerification };
}

async function loadAndValidateInputs(options, repoRoot) {
  requireOption(options, "manifest");
  requireOption(options, "out");
  if (options.customer !== DEFAULT_CUSTOMER) {
    throw new CliError(
      `Only ${DEFAULT_CUSTOMER} is supported by this executor today`,
    );
  }
  if (options.rollback && options.activate) {
    throw new CliError(
      "--rollback cannot be combined with --activate or --activate-only",
      2,
    );
  }
  if (options.activate || options.rollback) {
    requireOption(options, "evidenceDir");
  }

  const manifestPath = path.resolve(repoRoot, options.manifest);
  const manifest = await readJson(manifestPath);
  validateRuntimeManifest(manifest);
  if (manifest.customer_key !== options.customer) {
    throw new CliError(
      `manifest customer_key ${manifest.customer_key} does not match ${options.customer}`,
    );
  }

  let activationGate = null;
  if (options.evidenceDir) {
    activationGate = validateCustomerConfigActivationGate({
      customer: options.customer,
      manifest: options.manifest,
      evidenceDir: options.evidenceDir,
      repoRoot,
    });
  }

  return {
    manifestPath,
    manifest,
    activationGate,
  };
}

async function writeReport({
  options,
  manifestPath,
  manifest,
  activationGate,
  backendURL,
  operations,
  results,
  effectiveSessionVerification,
  executed,
}) {
  await mkdir(options.out, { recursive: true });
  const summary = manifestSummary(manifest);
  const manifestSha256 = `sha256:${sha256File(manifestPath)}`;
  const report = {
    generatedAt: new Date().toISOString(),
    customerKey: summary.customerKey,
    revision: summary.revision,
    executed,
    activate: Boolean(options.activate),
    activateOnly: Boolean(options.activateOnly),
    rollback: Boolean(options.rollback),
    manifest: manifestPath,
    manifestSha256,
    evidenceDir: options.evidenceDir || "",
    backendEndpointAlias: backendURL || "",
    activationGateChecked: Boolean(activationGate),
    manifestSummary: summary,
    operations: operations.map((operation) => ({
      key: operation.key,
      method: operation.method,
      summary: operation.summary,
    })),
    results,
    effectiveSessionVerification,
    noRawFileUpload: true,
    noDirectDatabaseWrite: true,
    noSchemaOrMigrationChange: true,
    noBusinessDataImport: true,
    noWorkflowFactRuntimeWrite: true,
  };
  await writeJson(
    path.join(options.out, "customer-config-release-report.json"),
    report,
  );
  await writeFile(
    path.join(options.out, "customer-config-release-report.md"),
    [
      "# Customer Config Release Report",
      "",
      `| executed | ${executed} |`,
      `| activate | ${Boolean(options.activate)} |`,
      `| activateOnly | ${Boolean(options.activateOnly)} |`,
      `| rollback | ${Boolean(options.rollback)} |`,
      `| customerKey | ${summary.customerKey} |`,
      `| revision | ${summary.revision} |`,
      `| manifest | ${manifestPath} |`,
      `| manifestSha256 | ${manifestSha256} |`,
      `| evidenceDir | ${options.evidenceDir || ""} |`,
      `| backendEndpointAlias | ${backendURL || ""} |`,
      `| activationGateChecked | ${Boolean(activationGate)} |`,
      `| operationCount | ${operations.length} |`,
      `| effectiveSessionVerified | ${effectiveSessionVerification?.status === "verified"} |`,
      "",
      "This report is generated by the customer config release executor. The executor uses JSON-RPC customer_config APIs only when --execute is explicitly set. It does not upload raw customer files, write database tables directly, generate migrations, import business data, or write Workflow / Fact runtime state.",
      "",
    ].join("\n"),
    "utf8",
  );
  return report;
}

export async function runCustomerConfigRelease(options, runtime = {}) {
  options = {
    customer: DEFAULT_CUSTOMER,
    execute: false,
    activate: false,
    activateOnly: false,
    rollback: false,
    ...options,
  };
  const repoRoot = runtime.repoRoot || process.cwd();
  const { manifestPath, manifest, activationGate } =
    await loadAndValidateInputs(options, repoRoot);
  if (
    options.execute &&
    !(options.backendURL || process.env.CUSTOMER_CONFIG_BACKEND_URL)
  ) {
    throw new CliError(
      "execution requires --backend-url or CUSTOMER_CONFIG_BACKEND_URL",
    );
  }
  const operations = buildOperations({
    manifest,
    activate: Boolean(options.activate),
    activateOnly: Boolean(options.activateOnly),
    rollback: Boolean(options.rollback),
  });
  const backendURL = options.execute
    ? normalizeBaseURL(
        options.backendURL || process.env.CUSTOMER_CONFIG_BACKEND_URL,
      )
    : "";
  const results = options.execute
    ? await executeOperations({
        backendURL,
        operations,
        manifest,
        activate: Boolean(options.activate),
        rollback: Boolean(options.rollback),
      })
    : { results: [], effectiveSessionVerification: null };
  return writeReport({
    options,
    manifestPath,
    manifest,
    activationGate,
    backendURL,
    operations,
    results: results.results,
    effectiveSessionVerification: results.effectiveSessionVerification,
    executed: Boolean(options.execute),
  });
}

async function runCli() {
  const options = parseCliArgs(process.argv.slice(2));
  if (options.help) {
    console.log(USAGE);
    return 0;
  }
  if (options.printInputTemplate) {
    console.log(JSON.stringify(buildInputTemplate(options.customer), null, 2));
    return 0;
  }
  const report = await runCustomerConfigRelease(options);
  console.log(
    `customer config release report: ${path.join(options.out, "customer-config-release-report.json")}`,
  );
  console.log(`executed: ${report.executed}`);
  console.log(`activate: ${report.activate}`);
  console.log(`rollback: ${report.rollback}`);
  console.log(`operationCount: ${report.operations.length}`);
  return 0;
}

const currentFile = fileURLToPath(import.meta.url);
if (process.argv[1] && path.resolve(process.argv[1]) === currentFile) {
  runCli()
    .then((code) => {
      process.exitCode = code;
    })
    .catch((error) => {
      if (error instanceof CliError) {
        console.error(error.message);
        process.exitCode = error.exitCode;
        return;
      }
      console.error(error);
      process.exitCode = 1;
    });
}

