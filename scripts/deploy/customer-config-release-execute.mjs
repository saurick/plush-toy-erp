class CliError extends Error {
  constructor(message, exitCode = 1) {
    super(message);
    this.name = "CliError";
    this.exitCode = exitCode;
  }
}

export function parseCliArgs(argv) {
  const options = {
    customer: DEFAULT_CUSTOMER,
    execute: false,
    activate: false,
    activateOnly: false,
    rollback: false,
    printInputTemplate: false,
    help: false,
  };
  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];
    if (token === "--help" || token === "-h") {
      options.help = true;
      continue;
    }
    if (token === "--execute") {
      options.execute = true;
      continue;
    }
    if (token === "--activate") {
      options.activate = true;
      continue;
    }
    if (token === "--activate-only") {
      options.activate = true;
      options.activateOnly = true;
      continue;
    }
    if (token === "--rollback") {
      options.rollback = true;
      continue;
    }
    if (token === "--print-input-template") {
      options.printInputTemplate = true;
      continue;
    }
    if (!token.startsWith("--")) {
      throw new CliError(`Unexpected argument: ${token}`, 2);
    }
    const equalIndex = token.indexOf("=");
    const key = token.slice(2, equalIndex === -1 ? undefined : equalIndex);
    const inlineValue =
      equalIndex === -1 ? undefined : token.slice(equalIndex + 1);
    const value = inlineValue ?? argv[index + 1];
    if (inlineValue === undefined) {
      index += 1;
    }
    if (value === undefined || String(value).startsWith("--")) {
      throw new CliError(`Missing value for --${key}`, 2);
    }
    switch (key) {
      case "manifest":
        options.manifest = value;
        break;
      case "out":
        options.out = value;
        break;
      case "evidence-dir":
        options.evidenceDir = value;
        break;
      case "backend-url":
        options.backendURL = value;
        break;
      case "customer":
        options.customer = value;
        break;
      default:
        throw new CliError(`Unknown option --${key}`, 2);
    }
  }
  return options;
}

